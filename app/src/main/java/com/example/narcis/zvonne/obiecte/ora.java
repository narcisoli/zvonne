public class ora {
    private int h;
    private int m;

    public ora() {
    }

    public ora(int h, int m) {
        this.h = h;
        this.m = m;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }
}
